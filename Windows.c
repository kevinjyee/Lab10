struct State {
  unsigned long x;      // x coordinate
  unsigned long y;      // y coordinate
  const unsigned char *image; // ptr->image
  long life;            // 0=dead, 1=alive
};          
typedef struct State STyp;

STyp Window1[3];
void Window1_Init(void){int i;
	for(i=0; i<3; i++){
		Window1[i].x =30*i +13;
		Window1[i].y = 11;
		Window1[i].image = window;
		Window1[i].life = 3;
	}
}
	
void Window1_Display(void){int i;
	for(i=0; i <3; i++){
		if (Window1[i].life != 0){
			LCD_PrintBMP(Window1[i].x, Window1[i].y, window, 0);
			LCD_DisplayBuffer();
		}
	}
}
